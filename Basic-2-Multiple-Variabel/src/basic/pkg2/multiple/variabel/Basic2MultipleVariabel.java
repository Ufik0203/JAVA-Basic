public class Basic2MultipleVariabel {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int a = 2, b = 5, c =  10;
        String A = "Hello", B = "World";
        int z = a + b + c;
        
        System.out.println("Number = " + a + b + c);
        System.out.println("Number = " + a + "," + b + "," + c);
        System.out.println("Count = " +( a + b + c));
        System.out.println("Word = " + A + B);
        System.out.println("Z = " + z);
        
        // its same like other variable, and its basic of multiple variable
        
    }
    
}
